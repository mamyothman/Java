public class Kitten{
	public String name;
	public String owner;
	public int age;
  	public Kitten(String name,String owner,int age){
  		this.name=name;
  		this.owner=owner;
  		this.age=age;
 	}
	public String getname(){
		return name;
	}
	public void setname(String name){
		this.name = name;
	}
  	public int getAge(){
  	return age;
	}
 	public void Age(){
  	this.age = age;
 	}
}   